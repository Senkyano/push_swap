#include "lib_push_swap.h"

void	printite(char *str, char c)
{
	int	i;

	i = -1;
	while (str[++i])
		write(1, &str[i], 1);
	if (c == 'a' || c == 'b')
		write(1, &c, 1);
	write(1, "\n", 2);
}
